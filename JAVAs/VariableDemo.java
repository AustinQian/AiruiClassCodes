public class VariableDemo {
    public static void main(String[] args) {
        int i;
        i = 1;
        System.out.println(i); //int类型

        byte p = 20;
        System.out.println(p); //byte类型

        short q = 30;
        System.out.println(q); //short类型

        float n = 32.13f;
        System.out.println(n); //float类型

        double m = 5.12;
        System.out.println(m); //double类型

        boolean b = true;
        System.out.println(b); //boolean类型
    }
}
